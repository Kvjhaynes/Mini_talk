#include <unistd.h>
#include <stdlib.h>
#include <stdio.h>
#include <signal.h>
#include "./ft_printf/ft_printf.h"

int	ft_strlen(char *s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	ft_atoi(const char *str)
{
	long			res;
	long			sign;
	unsigned int	i;

	res = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return ((int)(res * sign));
}

void	send_bit(int pid, char *str, size_t len)
{
	size_t	i;
	int		j;

	i = 0;
	while (i <= len)
	{
		j = 0;
		while (j < 7)
		{
			if (str[i] & 1)
				kill(pid, SIGUSR2);
			else
				kill(pid, SIGUSR1);
			str[i] >>= 1;
			j++;
			usleep(300);
		}
		i++;
	}
}

int	main(int argc, char **argv)
{
	int		pid;
	char	*str;

	if (argc == 3)
	{
		pid = ft_atoi(argv[1]);
		str = argv[2];
		send_bit(pid, str, ft_strlen(str));
	}
	else
		ft_printf("\n INVALID PARAMATERS! \n");
}
